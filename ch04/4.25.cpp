#include <iostream>

using namespace std;

int main()
{
	cout << "size of int " << sizeof(int) << endl;
	cout << "size of char " << sizeof(char) << endl;
	cout << "size of string " << sizeof(string) << endl;
	cout << "size of pointer " << sizeof(int*) << endl;
	cout << "size of bool " << sizeof(bool) << endl;
	
	return 0;
}